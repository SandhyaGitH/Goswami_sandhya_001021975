    private void DeleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteJButtonActionPerformed

         int selectedRow = enterpriseJTable.getSelectedRow();
        if(selectedRow >= 0){
             Restaurant restaurant = (Restaurant) enterpriseJTable.getValueAt(selectedRow, 3);
           
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to remove this Restaurant!", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
               Location lc =(Location) enterpriseJTable.getValueAt(selectedRow, 4) ;
                int index= lc.getRestaurantDirectory().getRestaurantList().indexOf(restaurant);
                lc.getRestaurantDirectory().getRestaurantList().remove(index);
                populateTable();
            
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row from table first.", "Warning",JOptionPane.WARNING_MESSAGE);
            
        }

    }//GEN-LAST:event_DeleteJButtonActionPerformed

