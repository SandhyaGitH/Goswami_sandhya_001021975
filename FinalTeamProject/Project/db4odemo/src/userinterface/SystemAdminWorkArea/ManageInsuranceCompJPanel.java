    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (Location network : system.getNetworkList()) {
            for (Restaurant restaurant : network.getRestaurantDirectory().getRestaurantList()) {
                Object[] row = new Object[6];
                row[0] = restaurant.getName();
                row[1] = network.getName();
                row[2]="All";
                row[3] = restaurant;
                row[4] = network;

                model.addRow(row);
            }
        }
       enterpriseJTable.getColumnModel().getColumn(3).setMaxWidth(0); 
       enterpriseJTable.getColumnModel().getColumn(4).setMaxWidth(0); 
    }

