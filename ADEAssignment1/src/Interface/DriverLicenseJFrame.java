    private void Btn_UploadPicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_UploadPicActionPerformed
         
         //jFileChooser1.setVisible(true);
         //jFileChooser1.setDialogTitle("Choose Picture");
         JFileChooser jFileChooser = new JFileChooser();
         jFileChooser.setDialogTitle(("Choose Picture"));
         int returnCode = jFileChooser.showOpenDialog(this);
         if(returnCode == JFileChooser.APPROVE_OPTION)
         {
             File file= jFileChooser.getSelectedFile();
             BufferedImage BuffIm;
             try{
                 BuffIm = ImageIO.read(file);
                 ImageIcon img = new ImageIcon(BuffIm);
                 person.setPicture(img);
                 //person.setIcon(img);
             }
             catch(IOException e)
             {}
             this.pack();
         }
             
             
         
         
    }//GEN-LAST:event_Btn_UploadPicActionPerformed

